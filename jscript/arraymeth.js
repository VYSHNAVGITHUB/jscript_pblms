var add=(num1,num2)=>num1+num2
var sub=(n1,n2)=>n1-n2
var mul=(no1,no2)=>no1*no2
var div=(a,b)=>a/b
var cube=(m)=>m**3

console.log(add(5,5));
console.log(sub(20,10));
console.log(mul(5,5));
console.log(div(10,5));
console.log(cube(10));

//max of two

var maxTwo=(a1,b1)=>a1>b1?a1:b1
console.log(maxTwo(100,250));

// smart sub

var smartsub=(x,y)=>x>y?x-y:y-x
console.log(smartsub(20,50));